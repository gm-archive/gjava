    private fileclass.File file;
    
    public ImageDisplayer(fileclass.File file) {
        initComponents();
        this.file = file;
    }
    
    @Override
    public int getWidth(){
        if(file.value==null)
            return 0;
        return ((ImageIcon) file.value).getIconWidth();
    }
    
    @Override
    public int getHeight(){
        if(file.value==null)
            return 0;
        return ((ImageIcon) file.value).getIconHeight();
    }
    
    @Override
    public Dimension getPreferredSize(){
        return new Dimension(getWidth(), getHeight());
    }
    
    @Override
    public void paint(Graphics g){
        
        //super.paint(g);
        boolean isBlack = true;
        for(int i = 0; i <= getWidth() / 10; i++){
            boolean isNBlack = isBlack;
            for(int j = 0; j <= getHeight() / 10; j++){
                if((isBlack=!isBlack))
                    g.setColor(Color.WHITE);
                else
                    g.setColor(Color.BLACK);
                g.fillRect(i * 10 + 1, j * 10 + 1, 10, 10);
            }
            isBlack = !isNBlack;
        }
        if(file.value!=null)
            g.drawImage(((ImageIcon) file.value).getImage(), 0, 0, ((ImageIcon) file.value).getImageObserver());
    }
    
