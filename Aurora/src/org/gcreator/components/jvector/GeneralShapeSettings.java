    public void updateValues() {
        if (shape != null) {
            jSpinner1.setValue(shape.getX());
            jSpinner2.setValue(shape.getY());
            jSpinner3.setValue(shape.getWidth());
            jSpinner4.setValue(shape.getHeight());
        } else {
            Double d = new Double(0);
            jSpinner1.setValue(d);
            jSpinner2.setValue(d);
            jSpinner3.setValue(d);
            jSpinner4.setValue(d);
        }
    }
    
