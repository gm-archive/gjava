    public void setShape(JVectorable j) {
        this.shape = j;
        updateValues();
        if (shape != null) {
            jScrollPane1.setViewportView(shape.getSpecialValuesPanel());
            if (shape.allowsNegativeSize()) {
               ((SpinnerNumberModel)jSpinner3.getModel()).setMinimum(null);
               ((SpinnerNumberModel)jSpinner4.getModel()).setMinimum(null);
            } else {
                ((SpinnerNumberModel)jSpinner3.getModel()).setMinimum(new Double(0));
               ((SpinnerNumberModel)jSpinner4.getModel()).setMinimum(new Double(0));
            }
        } else {
            jScrollPane1.setViewportView(null);
        }
        setEnabled(j != null);
    }
    public JVectorable getShape() {
        return shape;
    }
    
