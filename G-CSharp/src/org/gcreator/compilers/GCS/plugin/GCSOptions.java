    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            File x = new File(".gcs");
            x.createNewFile();
            FileWriter w = new FileWriter(x);
            if (jComboBox1.getSelectedItem().equals("Visual Studio C# [csc]")) {
                w.write('V');
            } else if (jComboBox1.getSelectedItem().equals("Mono C# 1.0 [mcs]")) {
                w.write('M');
            } else if (jComboBox1.getSelectedItem().equals("Mono C# 2.0 [gmcs] (Recommended)")) {
                w.write('G');
            }
            w.write(jTextField1.getText());
            w.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed
