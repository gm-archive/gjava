    JPanel curpanel = null;
    
    public void gotoPhase(int num){
        if(num==1){
            InitialPanel panel = new InitialPanel(this);
            setPanel(panel);
            curpanel = panel;
        }
        if(num==2){
            LicensePanel panel = new LicensePanel(this);
            setPanel(panel);
            curpanel = panel;
        }
        if(num==3){
            ChoosePathPanel panel = new ChoosePathPanel(this);
            setPanel(panel);
            curpanel = panel;
        }
        phase = num;
    }
    
