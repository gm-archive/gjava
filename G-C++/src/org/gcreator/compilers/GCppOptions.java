private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
    if(jComboBox1.getSelectedItem().equals("GNU Compiler Collection [gcc]"))
        jTextField1.setEnabled(false);
    else if(jComboBox1.getSelectedItem().equals("Minimalistic GNU for Windows [MinGW]")){
        jTextField1.setEnabled(true);
        if(jTextField1.getText().equals(""))
            jTextField1.setText("C:/MinGW/bin/");
    }
}//GEN-LAST:event_jComboBox1ActionPerformed

private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try{
        File x = new File(".gcpp");
        x.createNewFile();
        FileWriter w = new FileWriter(x);
        if(jComboBox1.getSelectedItem().equals("GNU Compiler Collection [gcc]"))
            w.write('G');
        else if(jComboBox1.getSelectedItem().equals("Minimalistic GNU for Windows [MinGW]"))
            w.write('M');
            w.write(jTextField1.getText());
        w.close();
    }
    catch(Exception e){
        System.out.println(e.getMessage());
    }
}//GEN-LAST:event_jButton1ActionPerformed
    
    
